!-----------------------------------------------------------------------------------------------!
SUBROUTINE DSCAL(DSG,ST1,ST2,ST3,ST4,NP,ITYPE)
!-----------------------------------------------------------------------------------------------!
IMPLICIT NONE
INTEGER :: NP,ITYPE,NPM1,I,IW,NXL
DOUBLE PRECISION :: DSG(NP),ST1,ST2,ST3,ST4,SZ(NP),DIVS,PI,ONE
!-----------------------------------------------------------------------------------------------!
NPM1   =NP-1
DSG(1) =0.D0
DSG(NP)=1.D0
!-----------------------------------------------------------------------------------------------!
SELECT CASE (ITYPE)
!-----------------------------------------------------------------------------------------------!
!    Equidistant                                                                                !
!-----------------------------------------------------------------------------------------------!
   CASE(1)
      DIVS=1.D0/DFLOAT(NPM1)
      DO I=2,NP-1
         DSG(I)=DFLOAT(I-1)*DIVS
      END DO !I=2,NP-1
!-----------------------------------------------------------------------------------------------!
!    One-Sided Streching Function at I=1                                                        !
!-----------------------------------------------------------------------------------------------!
   CASE(2)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=DIVS/ST1
      CALL STRET(SZ,ST1,NP)
      DO I=2,NPM1
         DSG(I)=SZ(I)
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    One-Sided Streching Function at I=NX                                                       !
!-----------------------------------------------------------------------------------------------!
   CASE(3)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=DIVS/ST1
      CALL STRET(SZ,ST1,NP)
      DO I=2,NPM1
         IW=NP+1-I
         DSG(IW)=1.D0-SZ(I)
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Two-Sided Streching Function                                                               !
!-----------------------------------------------------------------------------------------------!
   CASE(4)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=DIVS/ST1
      ST2=DIVS/ST2
      CALL STRET2(SZ,ST1,ST2,NP)
      DO I=2,NPM1
         DSG(I)=SZ(I)
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Geometric Progression with S1 at I=1                                                       !
!-----------------------------------------------------------------------------------------------!
   CASE(5)
      DIVS=1.D0/DFLOAT(NPM1)
      DO I=2,NPM1
         DSG(I)=(DFLOAT(I-1)*DIVS)**ST1
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Geometric Progression with S1 at I=NX                                                      !
!-----------------------------------------------------------------------------------------------!
   CASE(6)
      DIVS=1.D0/DFLOAT(NPM1)
      DO I=2,NPM1
         IW=NP+1-I
         DSG(IW)=1.D0-(DFLOAT(I-1)*DIVS)**ST1
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Cosine Function                                                                            !
!-----------------------------------------------------------------------------------------------!
   CASE(7)
      ONE=1.D0
      PI =4.D0*DATAN(ONE)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=ST1*PI/180.D0
      ST2=PI-ST2*PI/180.D0
      DO I=2,NPM1
         SZ(I)=ST1+DFLOAT(I-1)*DIVS*(ST2-ST1)
         DSG(I)=(DCOS(ST1)-DCOS(SZ(I)))/(DCOS(ST1)-DCOS(ST2))
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Half-Cosine Function Stretching at I=1                                                     !
!-----------------------------------------------------------------------------------------------!
   CASE(8)
      ONE=1.D0
      PI =4.D0*DATAN(ONE)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=ST1*PI/180.D0
      DO I=2,NPM1
         SZ(I)=DFLOAT(I-1)*DIVS*(PI/2.D0-ST1)+ST1
         DSG(I)=1.D0-DCOS(SZ(I))/DCOS(ST1)
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Half-Cosine Function Stretching at I=NX                                                    !
!-----------------------------------------------------------------------------------------------!
   CASE(9)
      ONE=1.D0
      PI =4.D0*DATAN(ONE)
      DIVS=1.D0/DFLOAT(NPM1)
      ST1=ST1*PI/180.D0
      DO I=2,NPM1
         IW=NP+1-I
         SZ(I)=DFLOAT(I-1)*DIVS*(PI/2.D0-ST1)+ST1
         DSG(IW)=DCOS(SZ(I))/DCOS(ST1)
      END DO !I=2,NPM1
!-----------------------------------------------------------------------------------------------!
!    Double-Cosine Function                                                                     !
!-----------------------------------------------------------------------------------------------!
   CASE(10)
      ONE=1.D0
      PI =4.D0*DATAN(ONE)
      NXL=(NP+1)/2
      DIVS=1.D0/DFLOAT(NXL-1)
      ST1=ST1*PI/180.D0
      ST2=PI-ST2*PI/180.D0
      DO I=2,NXL-1
         SZ(I)=ST1+DFLOAT(I-1)*DIVS*(ST2-ST1)
         DSG(I)=(DCOS(ST1)-DCOS(SZ(I)))/(DCOS(ST1)-DCOS(ST2))/2.D0
      END DO !I=2,NXL-1
      DSG(NXL)=0.5D0
      ST3=ST3*PI/180.D0
      ST4=PI-ST4*PI/180.D0
      DO I=2,NXL-1
         SZ(I)=ST3+DFLOAT(I-1)*DIVS*(ST4-ST3)
         DSG(NXL-1+I)=0.5D0+(DCOS(ST3)-DCOS(SZ(I)))/(DCOS(ST3)-DCOS(ST4))/2.D0
      END DO !I=2,NXL-1
!-----------------------------------------------------------------------------------------------!
END SELECT !(ITYPE)
!-----------------------------------------------------------------------------------------------!
END SUBROUTINE DSCAL
